$(document).ready(function(){
   $("#connectHome").click(function(){
       $(".connectBlock").show();
       $(".welcome").hide();
       $(".signBlock").hide();
   });
   $('#signHome').click(function(){
       $('.connectBlock').hide();
       $('.welcome').hide();
       $('.signBlock').show();
       $('.errorConnect').hide();
   });
   $('.backHome').click(function(){
        location.reload();
   });
   $('#passMailRescue').click(function(){
      $('.load').show(); 
   });
   $('#idMailRescue').click(function(){
      $('.load').show(); 
   });
});

